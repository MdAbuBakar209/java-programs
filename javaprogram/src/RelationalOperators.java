public class RelationalOperators {
    public static void main(String[] args) {
        int a=10,b=20;
        /* Bitwise or relational operators */
        System.out.println(a==b);
        System.out.println(a !=b);
        System.out.println(a <=b);
        System.out.println(a >=b);
        System.out.println(a<b);
        System.out.println(a>b);
    }

}
